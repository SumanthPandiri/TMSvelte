<script lang="ts">
	import { onMount } from 'svelte';
	import { getContext } from 'svelte';
	const { close } = getContext('simple-modal');

	export let onFinish = () => {};

	const width = 224;
	let height = 224;

	let streaming = false;

	let video: HTMLVideoElement = null;
	let canvas: HTMLCanvasElement = null;
	let photo = null;
	let button = null;

	let images: [HTMLImageElement] = [];

	const _onFinish = () => {
		onFinish(images);
		close();
	};

	onMount(async () => {
		try {
			let stream = await navigator.mediaDevices.getUserMedia({ video: true, audio: false });

			video.srcObject = stream;
			video.play();
			clearPhoto();
		} catch (error) {
			console.error(error);
		}
	});

	const takePicture = () => {
		const context = canvas.getContext('2d');
		if (width && height) {
			canvas.width = width;
			canvas.height = height;
			context?.drawImage(video, 0, 0, width, height);

			const data = canvas.toDataURL('image/png');
			photo.setAttribute('src', data);

			let image: HTMLImageElement = new Image();
			image.src = data;

			images = [image, ...images];
		}
	};

	const handleCanPlay = () => {
		if (!streaming) {
			// height = (video.videoHeight / video.videoWidth) * width;
			video.setAttribute('width', width);
			video.setAttribute('height', height);
			canvas.setAttribute('width', width);
			canvas.setAttribute('height', height);
			streaming = true;
		} else {
			clearPhoto();
		}
	};

	const clearPhoto = () => {
		const context = canvas.getContext('2d');
		context.fillStyle = '#AAA';
		context.fillRect(0, 0, canvas.width, canvas.height);

		const data = canvas.toDataURL('image/png');
		photo.setAttribute('src', data);
	};
</script>

<div class="space-y-6">
	<div class="flex h-full w-full flex-col items-center space-y-6">
		<video bind:this={video} src="" class="rounded-md" id="video" on:canplay={handleCanPlay}
			>Video stream not availible</video>
		<div class="flex space-x-3">
			<button class="btn-secondary btn" on:click={takePicture}>Capture</button>
			<button class="btn-success btn" on:click={_onFinish}>Done</button>
		</div>
	</div>

	<canvas bind:this={canvas} id="canvas" class="hidden" />

	<div class="output hidden">
		<img bind:this={photo} id="photo" alt="The screen capture will appear in this box." />
	</div>
	<p class="text-right">Images: {images.length}</p>

	<div class="flex space-x-1 overflow-scroll">
		{#each images as image}
			<img src={image.src} alt="" class="h-24 rounded-md" />
		{/each}
	</div>
</div>
